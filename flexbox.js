
function maxi(){
	theNote = document.getElementById("theNote");
	
	theNote.style.display = "block";
	theNote.style.position = "fixed";
	theNote.style.width = "100%";
	theNote.style.left = "0";
	theNote.style.top = "0";
	
}


function minix(){
	
	theNote = document.getElementById("theNote");
	
	theNote.style.display = "inline-block";
	theNote.style.position = "relative";
	theNote.style.clear = "right";


	
}

